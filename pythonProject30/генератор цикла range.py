# range - переводиться как диапазон. Синтаксис:
# for (переменная) in range ? а далее идёт указание с какого элемента начать(start, stop, и (шаг)-step )
# Пример:
for i in range(5):
    print(i, end = "")# При чём выведутся числа с нуля до четырёх , и при печатании простьо print (i) - они будут в столбик ,а конструкция end=" " -позволяет их выводить в строчку
    